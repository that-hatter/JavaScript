function compare (rowNum, rows) {
	const row = rows[rowNum]
	const rowBelow = rows[rowNum + 1];
	for (let i = 0; i < row.length; i++) {
		row[i] += (rowBelow[i] > rowBelow[i + 1]) ? rowBelow[i] : rowBelow[i + 1];
	}
	return rows;
}

